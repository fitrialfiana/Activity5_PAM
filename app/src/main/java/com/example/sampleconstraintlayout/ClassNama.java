package com.example.sampleconstraintlayout;


public class ClassNama {
    private String Nama;

    //Inisialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }
    //Membuat method getName untuk mengembaikan nilai ke variabel Nama
    public String getName()
    {
        return this.Nama;
    }
}